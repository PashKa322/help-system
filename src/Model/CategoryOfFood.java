package Model;

public class CategoryOfFood {

    private String name;

    public CategoryOfFood(String name){
        this.name = name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return "Name: " + name;
    }


}
